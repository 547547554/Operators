#include<iostream>
using namespace std;

//{==,>,<,>=,<=,!=}

int main()
{
    //comparision operator
    // 0 or 1

   int a, b;
   cout<<"Enter a : ";
   cin>>a;

   cout<<"Enter b : ";
   cin>>b;

   // ==
   if(a==b)
   cout<<"Yes";
   else
   cout<<"No";
   cout<<endl;

    // >
   if(a>b)
   cout<<"Yes";
   else
   cout<<"No";
   cout<<endl;

    // <
   if(a<b)
   cout<<"Yes";
   else
   cout<<"No";
   cout<<endl;

    // >=
   if(a>=b)
   cout<<"Yes";
   else
   cout<<"No";
   cout<<endl;

    // <=
   if(a <=b)
   cout<<"Yes";
   else
   cout<<"No";
   cout<<endl;

    // !=
   if(a!=b)
   cout<<"Yes";
   else
   cout<<"No";
   cout<<endl;


}